public class DadosAdicionais {
    private String informacoesComplementares;
    private String reservadoFisico;

    public String getInformacoesComplementares() {
        return informacoesComplementares;
    }

    public void setInformacoesComplementares(String informacoesComplementares) {
        this.informacoesComplementares = informacoesComplementares;
    }

    public String getReservadoFisico() {
        return reservadoFisico;
    }

    public void setReservadoFisico(String reservadoFisico) {
        this.reservadoFisico = reservadoFisico;
    }
}
